package Modelo;

/**
 *
 * @author LENOVO
 */
public class PasajeroAdulto extends PasajeroNino {
    
    private String telefono;
    private String correo;
    private String tipo="Cedula ciudadania";
    public PasajeroAdulto(String telefono, String correo, String nombre, String Apellido, int documento, String sexo) {
        super(nombre, Apellido, documento, sexo);
        this.telefono = telefono;
        this.correo = correo;
    }

    /**
     * @return the telefono
     */
    public String getTelefono() {
        return telefono;
    }

    /**
     * @param telefono the telefono to set
     */
    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    /**
     * @return the correo
     */
    public String getCorreo() {
        return correo;
    }

    /**
     * @param correo the correo to set
     */
    public void setCorreo(String correo) {
        this.correo = correo;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
}
